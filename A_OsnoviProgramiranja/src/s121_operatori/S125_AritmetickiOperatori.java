package s121_operatori;

public class S125_AritmetickiOperatori {

	public static void main(String[] args) {
		
		int a = 100, b = 40;
		
		System.out.println("Rezultat a + b: " + (a + b));
		System.out.println("Rezultat a - b: " + (a - b));
		System.out.println("Rezultat a * b: " + (a * b));
		System.out.println("Rezultat a / b: " + (a / b));
		System.out.println("Rezultat a % b: " + (a % b));

	}

}
